a= int(input("Enter the number : "))
b= int(input("Enter the number : "))
operations = input("Enter the operation : multiply/add/sub/divide")
c= str("multiply")
d= str("add")
e= str("sub")
f= str("divide")
if operations==c :
    print (a*b)
elif operations==d :
    print (a+b)
elif operations==e :
    print (a-b)
elif operations==f :
    print (a/b)

